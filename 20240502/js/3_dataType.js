document.addEventListener('DOMContentLoaded', function(){

  "use strict";

  // 숫자형
  let num = 123;
  num = 12.345;
  num = -5;
  num = Infinity;
  num = NaN;

  // 문자형
  let str = "hello";
  str = 'hello2';
  str = `hello3`;

  // 불린형
  let checked = true;
  checked = false;

  // null 값
  // 어떠한 자료형에도 속하지 않는 값
  // 즉, 빈 값(empty)이다. 값이 존재하지 않는다.
  let age = null;

  // undefined 값
  // null 값 처럼 자신만의 자료형을 형성한다.
  // 즉, 변수를 선언했으나 값이 할당되지 않은 상태
  let name; // <- undefined

  /* 
    객체형
    - 객체형을 제외한 다른 자료형은 문자열이든 숫자든 한 가지만 표현할 수 있기 때문에 
      원시 자료형이라 부른다. 
    - 반면 객체는 데이터 컬렉션이나 복잡한 개체를 표현할 수 있다. 

    심볼
    - 객체의 고유한 식별자를 만들 때 사용한다.
  */

});